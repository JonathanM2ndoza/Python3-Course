#Types of data in Python

#Strings
print('****************Strings***************')
print('Hello World')
print(type("Hello World"))
print('Hello World' + ''' and bye''')


print('****************Numbers***************')
#Integer 
print(41)

#Float
print(41.5)

#Boolean
print('****************Boolean***************')
print(type(True))
print(type(False))


#List (Mutable)
print('****************List (Mutable)***************')
print([1,5,8])
print(['Hi', """It's sunny"""])
print([1,"Hi",4.5,True])


#Tuples (Inmutable)
print('****************Tuples (Inmutable)***************')
print((1,9,52))

#Dictionaries 
print('****************Dictionaries***************')
print(type({
    'name':'Jonathan' ,
    'lastname':'Mendoza',
    'nick':'jona'
}))

print('****************None***************')
print(type(None))

